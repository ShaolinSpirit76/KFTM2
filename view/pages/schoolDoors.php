  <h3 id="schoolDoorsTitle" class="text-center">Histoire de l'école</h3>



  <p class="text-justify padding">Jean-Marie Levray, élève et instructeur de l'école du Pontet ayant dû quitter le Vaucluse pour ses activités professionnelles, s'installait à Montivilliers. Après avoir vainement cherché une école de kung-fu sérieuse pour continuer sa pratique, la décision fut prise de créer une école. </p>




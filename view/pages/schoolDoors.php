  <p class="text-justify padding">Notre salle est superbe &#8211; à la suite des travaux réalisés par notre instructeur, les élèves, mais aussi le propriétaire. Elle paraît prête à aborder la nouvelle saison. Malheureusement, la nature en a voulu autrement, et cette fin d&rsquo;année 1999 est une catastrophe. La pluie s&rsquo;est acharnée sur Montivilliers comme sur beaucoup d&rsquo;autres régions, et elle a plongé notre salle dans prés d&rsquo;un mètre d&rsquo;eau. Un désastre, les tatamis flottaient sur une eau boueuse, de quoi se décourager.</p>



  <p class="text-justify padding">Beaucoup d&rsquo;écoles de toutes sortes auraient fermé leurs portes à la suite d&rsquo;une telle épreuve, mais la nôtre est encore là et a retrouvé tout le charme d&rsquo;une salle de kung-fu que beaucoup d&rsquo;enseignants aimeraient avoir. Et tout cela grâce aux efforts déployés par notre instructeur, sa famille, les élèves et le propriétaire. Une grande partie de la rénovation a été financièrement possible grâce à la vente des calendriers à l&rsquo;effigie de l&rsquo;école. Toutes les écoles Thieulâm ont manifesté leur solidarité à cette occasion. À ce moment, notre école accueille prés de 70 élèves. Nous voyons l&rsquo;avenir sereinement, et pensons que si notre école a pu résister à cette épreuve imposée par la nature, elle est sur la bonne voie.</p>



  <p class="text-justify padding"><b>2003</b></p>



  <p class="text-justify padding">Décidément , notre école est destinée à subir des épreuves. Au mois de mai 2003, alors qu&rsquo;une grande partie de nos élèves se trouve au Pontet pour le Tournoi national Adultes, une nouvelle catastrophe s&rsquo;abat sur nous. Une nouvelle inondation, encore plus sévère que la première, dévaste nos locaux. Nous apprenons la nouvelle, impuissants, alors que nos compétiteurs sont en train d&rsquo;évoluer. Profondément choqués et perturbés, notre équipe récolte malgré tout son lot de médailles. L&rsquo;ensemble des écoles Thieulâm nous ayant assuré de leur compassion et de leur solidarité, c&rsquo;est avec un certain envoûtement que nous repartons vers Montivilliers. Les dégâts sont à la mesure de notre imagination. Les dures journées de nettoyage commencent&#8230; Pratiquement tout le matériel est irrécupérable, et nous devons jeter les tatamis, le matériel de frappe, les meubles… Même les archives sont perdues…</p>




  <p class="text-justify padding"><b>1996 - 2003</b></p>



  <p class="text-justify padding">En 1996, les premiers cours se déroulent avec 5 élèves. Ils sont dirigés par Jean Marie et son fils Julien qui le seconde. Les entraînements se déroulent dans un gymnase &#8211; avant chaque cours, il faut descendre les tatamis qui sont logés à 3 m de hauteur (c&rsquo;est le début de l&rsquo;échauffement). Il faut les remettre en place à la fin de chaque cours.</p>



  <p class="text-justify padding">L&rsquo;année 1997 se fait également dans ce gymnase, en alternance avec deux autres gymnases ; difficile de gérer les déplacements. De nouveaux élèves arrivent, tous ne resteront pas. C&rsquo;est en 1998 que nous prenons en location une ancienne menuiserie, pleine de charme. D&rsquo;autres élèves arrivent, puis comme d&rsquo;habitude, la sélection naturelle se fait d&rsquo;elle-même. L&rsquo;année 1999 est mouvementée, mais riche d&rsquo;enseignements. En septembre, après de nombreux travaux, notre salle est prête à recevoir tous les élèves des trois dernières années, ainsi qu&rsquo;un nouveau cru 99/2000 riche en quantité.</p>





